// // string methods
// | Method          | Description                    |
// | --------------- | ------------------------------ |
// | `length`        | Returns string length          |
// | `toUpperCase()` | Convert to uppercase           |
// | `toLowerCase()` | Convert to lowercase           |
// | `charAt()`      | Returns char at index          |
// | `indexOf()`     | First index of substring       |
// | `lastIndexOf()` | Last index of substring        |
// | `includes()`    | Check if substring exists      |
// | `startsWith()`  | Check start                    |
// | `endsWith()`    | Check end                      |
// | `slice()`       | Extract part of string         |
// | `substring()`   | Extract part (no negatives)    |
// | `substr()`      | Extract by length (deprecated) |
// | `trim()`        | Remove spaces                  |
// | `trimStart()`   | Trim left                      |
// | `trimEnd()`     | Trim right                     |
// | `split()`       | Convert string to array        |
// | `replace()`     | Replace first occurrence       |
// | `replaceAll()`  | Replace all occurrences        |
